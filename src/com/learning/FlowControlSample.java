package com.learning;

public class FlowControlSample {
public static void main(String[] args) {
	if(1 >2) {
		System.out.println("Code evaluated inside if block");
	}else if(1<2){
		System.out.println("code executed inside else if block");
	}else {
		System.out.println("code executed in else block");
	}
	
}
}
